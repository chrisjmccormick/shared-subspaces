def train_one_epoch(model, loader, criterion, optimizer, device, epoch, config):
    model.train()
    running_loss = 0.0
    total_samples = 0

    # Define CUDA events to measure GPU flops only.
    start_event = torch.cuda.Event(enable_timing=True)
    end_event   = torch.cuda.Event(enable_timing=True)

    # Allow previous epoch to finish, then start the cuda timing.
    torch.cuda.synchronize()
    start_event.record()

    progress_bar = tqdm(loader, desc=f"Training Epoch {epoch+1}/{config['epochs']}")

    # Manual warmup logic.
    # If we're still in the warm up period,
    if epoch < config['warmup_epochs']:
        # Fraction of the warmup period
        lr_scale = (epoch + 1) / config['warmup_epochs']

        for g in optimizer.param_groups:
            g['lr'] = config['lr'] * lr_scale

    # For each batch of images...
    for images, labels in progress_bar:

        # Move to the GPU
        images, labels = images.to(device), labels.to(device)

        # Clear gradients
        optimizer.zero_grad()

        # Forward pass
        outputs = model(images)

        # Evaluate loss from predictions.
        loss = criterion(outputs, labels)

        # Backprop
        loss.backward()

        # Update weights.
        optimizer.step()

        # Update tracking values.
        running_loss += loss.item()
        total_samples += images.size(0)

        # TODO - Displays the wrong loss?
        progress_bar.set_postfix(loss=f"{loss.item():.4f}")

    # Stop the CUDA timer.
    end_event.record()
    torch.cuda.synchronize()

    elapsed_ms = start_event.elapsed_time(end_event)   # GPU time only

    samples_per_sec = total_samples / (elapsed_ms / 1_000)

    # After warm-up epochs, step the scheduler
    if epoch >= config['warmup_epochs']:
        scheduler.step()

    current_lr = optimizer.param_groups[0]['lr']

    # Log stats.
    wandb.log({
        "epoch": epoch,
        "learning_rate": current_lr,
        "train_samples_per_sec": samples_per_sec
    })

    return running_loss / len(loader)

def evaluate(model, loader, criterion, device):
    model.eval()
    correct = 0
    total = 0
    running_loss = 0.0
    with torch.no_grad():
        for images, labels in tqdm(loader, desc="Evaluating", leave=False):
            images, labels = images.to(device), labels.to(device)
            outputs = model(images)
            loss = criterion(outputs, labels)
            running_loss += loss.item()
            _, predicted = torch.max(outputs.data, 1)
            total += labels.size(0)
            correct += (predicted == labels).sum().item()
    accuracy = 100 * correct / total
    avg_loss = running_loss / len(loader)
    return avg_loss, accuracy

