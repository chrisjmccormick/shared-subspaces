"""## MLPDecomp

A modified version of the MLP from ViT.

It implements a simple decomposition of the input and output weight matrices. This creates the same concept of shared subspaces.

* On the input side:
  * The token down projection `w1a` is shared across all of the neurons, and receives gradient updates regardless of which neurons fire.
  * The (reduced-size) neuron input weights are in `w1b`.
* The same is true of the output side, in reverse.
"""

class MlpDecomp(nn.Module):
    """ MLP as used in Vision Transformer, MLP-Mixer and related networks

    NOTE: When use_conv=True, expects 2D NCHW tensors, otherwise N*C expected.
    """
    def __init__(
            self,
            in_features,
            latent_dim,
            hidden_features,
            out_features,
            act_layer=nn.GELU,
            norm_layer=None,
            bias=True,
            drop=0.,
            use_conv=False,
    ):
        super().__init__()

        #bias = to_2tuple(bias)
        #drop_probs = to_2tuple(drop)

        # Full MLP:
        #   - 192-dim token
        #   - 768 neurons
        #      - W1 is (192, 768) = (in_features, hidden_features)
        #      - W2 is (768, 192) = (hidden_features, out_features)
        #
        # Decomposed (example)
        #    - W1A is 192 x 64 <-- Bottlenecks the input to latent_dim
        #    - W1B is 64 x 768
        #
        #    - W2A is 768 x 64
        #    - W2B is 64 x 768


        self.w1a = nn.Linear(in_features, latent_dim, bias=False)
        self.w1b = nn.Linear(latent_dim, hidden_features, bias=bias)

        self.act = act_layer()

        #self.drop1 = nn.Dropout(drop_probs[0])
        #self.norm = norm_layer(hidden_features) if norm_layer is not None else nn.Identity()

        self.w2a = nn.Linear(hidden_features, latent_dim, bias=False)
        self.w2b = nn.Linear(latent_dim, out_features, bias=bias)

        #self.drop2 = nn.Dropout(drop_probs[1])

    def forward(self, x):

        x = self.w1a(x) # Shared down projection.
        x = self.w1b(x) #

        x = self.act(x)

        #x = self.drop1(x)
        #x = self.norm(x)

        x = self.w2a(x)
        x = self.w2b(x)

        #x = self.drop2(x)

        return x

